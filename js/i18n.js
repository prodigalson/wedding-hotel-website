/* ---- Language helpers ---- */
function getLang() {
  const saved = localStorage.getItem("wedding_lang");
  if (saved && I18N[saved]) return saved;
  const nav = (navigator.language || "en").toLowerCase();
  if (nav.startsWith("pt")) return "pt";
  if (nav.startsWith("it")) return "it";
  return "en";
}
function setLang(l) { localStorage.setItem("wedding_lang", l); }
function t(key, vars) {
  const lang = getLang();
  let s = (I18N[lang] && I18N[lang][key]) || I18N.en[key] || key;
  if (vars) for (const k in vars) s = s.replaceAll("{" + k + "}", vars[k]);
  return s;
}
function localeCode() { return { en: "en-US", pt: "pt-BR", it: "it-IT" }[getLang()]; }
function fmtDate(iso) {
  if (!iso) return "";
  const d = new Date(iso + "T12:00:00");
  return d.toLocaleDateString(localeCode(), { weekday: undefined, year: "numeric", month: "long", day: "numeric" });
}
function fmtDateRange(isoStart, isoEnd) {
  const d1 = new Date(isoStart + "T12:00:00"), d2 = new Date(isoEnd + "T12:00:00");
  const f = new Intl.DateTimeFormat(localeCode(), { year: "numeric", month: "long", day: "numeric" });
  if (typeof f.formatRange === "function") return f.formatRange(d1, d2);
  return f.format(d1) + " – " + f.format(d2);
}
function fmtDateLong(iso) {
  const d = new Date(iso + "T12:00:00");
  return d.toLocaleDateString(localeCode(), { weekday: "long", year: "numeric", month: "long", day: "numeric" });
}
function hotelText(hotel, base) {
  const lang = getLang();
  return hotel[base + "_" + lang] || hotel[base + "_en"] || "";
}
